# function = a block of code which is executed only when it is called.

def hello(first_name, last_name, age): # son los parametros de la funcion
    print("Hello " + first_name + " " + last_name)
    print("Have a nice day! ")
    print("You are " + str(age)) #hasta aqui acaba la funcion
    
hello("Luis", "Cota", 21)# aqui probamos la funcion con los argumentos
