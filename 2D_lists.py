# 2D lists = a list of lists

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza", "hamburger", "hotdog"]
dessert = ["cake", "ice cream"]

food  = [drinks, dinner, dessert] # junta las listas
print(food) 
print(food[1]) # se pone un index, solo imprime la lista seleccionada
print(food[1][1]) # imprime el elemnto 1 de la lista 1
