import math

 
pi = 3.1416
x = 5
y = 6
z = 134

print(round(pi))#redondea pi
print(math.ceil(pi))#redondea al siguiente numero entero en este caso es 4
print(math.floor(pi))#redondea al numero entero menor
print(abs(pi))#da el valor absoluto 
print(pow(pi, 3))#sirve para elevar a un potencia
print(math.sqrt(pi))#saca las raices de algo
print(max(x,y,z))#saca el maximo
print(min(x,y,z))#saca el minimo
